package Common;

public class IntPoint3 {
	/**
	 * @uml.property  name="x"
	 */
	int x;
	/**
	 * @uml.property  name="y"
	 */
	int y;
	/**
	 * @uml.property  name="z"
	 */
	int z;

	public IntPoint3() {
		this.x = this.y = this.z = 0;
	}

	public IntPoint3(int x, int y, int z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}

	/**
	 * @return
	 * @uml.property  name="x"
	 */
	public int getX() {
		return x;
	}

	/**
	 * @param x
	 * @uml.property  name="x"
	 */
	public void setX(int x) {
		this.x = x;
	}

	/**
	 * @return
	 * @uml.property  name="y"
	 */
	public int getY() {
		return y;
	}

	/**
	 * @param y
	 * @uml.property  name="y"
	 */
	public void setY(int y) {
		this.y = y;
	}

	/**
	 * @return
	 * @uml.property  name="z"
	 */
	public int getZ() {
		return z;
	}

	/**
	 * @param z
	 * @uml.property  name="z"
	 */
	public void setZ(int z) {
		this.z = z;
	}

}
