package Exports;

import java.util.HashMap;

public class PartColors {
	private HashMap<PartDomainT, Integer> colorMap;

	public PartColors() {
		colorMap = new HashMap<PartDomainT, Integer>();
	}

	public Integer getColorId(PartDomainT domain) {
		return colorMap.get(domain);
	}

	public Integer setColorId(PartDomainT domain, Integer color) {
		return colorMap.put(domain, color);
	}
}
