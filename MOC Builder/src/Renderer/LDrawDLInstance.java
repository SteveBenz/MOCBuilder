package Renderer;

public class LDrawDLInstance {
	/**
	 * @uml.property  name="next"
	 * @uml.associationEnd  
	 */
	LDrawDLInstance next;
	/**
	 * @uml.property  name="color"
	 */
	float color[];
	/**
	 * @uml.property  name="comp"
	 */
	float comp[];
	/**
	 * @uml.property  name="transform"
	 */
	float transform[];

	public LDrawDLInstance(){
		color = new float[4];
		comp = new float[4];
		transform = new float[16];
	}
	/**
	 * @return
	 * @uml.property  name="next"
	 */
	public LDrawDLInstance getNext() {
		return next;
	}

	/**
	 * @param next
	 * @uml.property  name="next"
	 */
	public void setNext(LDrawDLInstance next) {
		this.next = next;
	}

	/**
	 * @return
	 * @uml.property  name="color"
	 */
	public float[] getColor() {
		return color;
	}

	/**
	 * @param color
	 * @uml.property  name="color"
	 */
	public void setColor(float[] color) {
		this.color = color;
	}

	/**
	 * @return
	 * @uml.property  name="comp"
	 */
	public float[] getComp() {
		return comp;
	}

	/**
	 * @param comp
	 * @uml.property  name="comp"
	 */
	public void setComp(float[] comp) {
		this.comp = comp;
	}

	/**
	 * @return
	 * @uml.property  name="transform"
	 */
	public float[] getTransform() {
		return transform;
	}

	/**
	 * @param transform
	 * @uml.property  name="transform"
	 */
	public void setTransform(float[] transform) {
		this.transform = transform;
	}
}
