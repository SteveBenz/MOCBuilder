package Resource;

public enum SoundEffectT {	 
	ConnectingSuccess("ConnectingSuccess.wav"), ConnectingFail("ConnectingFail.wav");
	
	private static String DefaultFilePath = "/Resource/SoundEffect/";
	private String filePath;
	
	private SoundEffectT(String filePath){
		this.filePath = filePath;
	}
	public String getFilePath(){
		return DefaultFilePath+this.filePath;
	}
}
